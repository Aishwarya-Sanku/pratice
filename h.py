def join_with_alternating_parentheses(array, first_index, last_index):
    result = ""

    for i in range(first_index, last_index + 1):
        value = array[i]
        if (i - first_index) % 2 == 0:
            result += value
        else:
            if i != last_index:
                result += f"({value})."
            else:
                result += f"({value})"

    return result



